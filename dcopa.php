			<p class="ntcpar">Devapathiraja College Old Pupils’ Association (DCOPA) was established in 1968 under the guidance of late Mr. P. Wijewardena, the Principal at the time. Mr. G. N. Mendis, Mr. G. Deneththi, Mr. E. S. Kumarawadu, Mr. Hemasiri Karunasena, Mr. M. Vitharana and Mr. T. Amarasena were the pioneers of the DCOPA.  The main function of the DCOPA in its infancy was to provide scholarships for encourage students for academics and sports. The association abled to donate to the College Sir Ernest de Silva Memorial Building and it made a beautiful appearance to the College.</p>

