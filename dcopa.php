			<p class="ntcpar">DCOPA always takes pride in felicitating Old Pupils who have excelled in their respective disciplines and taken leadership in countless spheres of human activity. DCOPA has initiated a scholarship program with monthly cash grants, whole sponsorship for Annual Prize Giving and providing schoolbooks to the needy and deserving students of the College. Since its humble beginning, while achieving great accomplishments for the College, the Devapathiraja Collage Old Pupils’ Association stands tall and proud.</p>
